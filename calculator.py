def add(num1, num2):
    mySum = num1 + num2
    print("I've just added two numbers! My result is: " + str(mySum))
    return mySum


def substract(num1, num2):
    mySub = num1 - num2
    print("I've just substracted two numbers! My result is: " + str(mySub))
    return mySub


add(3, 4)
substract(5, 9)
